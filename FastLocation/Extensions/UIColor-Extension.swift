import UIKit

public extension UIColor {
    
    /// The accent color is the color that is used as the tint color of 
    /// the navigation bar, tab bar, map pins, and cell.imageView.
    ///
    /// - Returns: A UIColor
    public class func accentColor() -> UIColor {
        return #colorLiteral(red: 0.3019607843, green: 0.05882352941, blue: 0.6588235294, alpha: 1)
    }
    
}
